package Day11_switch;

public class switchStatement {
    public static void main(String[] args) {

        int z = 10;

        switch (z){

            case 10:
                System.out.println("Monday");

            case 11:
                System.out.println("Tuesday");

            case 12:
                System.out.println("Wednesday");


            default:
                System.out.println("Invalid number");



        }
    }
}
